class OperatorOverloading{
    int a = 5; int b= 7;
    public static void main(String arg[]){
        OperatorOverloading obj = new OperatorOverloading();
        System.out.println("a + b = "+obj.a+obj.b);//for concat
        System.out.println("a + b = " +(obj.a+obj.b));//For addition
    }
}

